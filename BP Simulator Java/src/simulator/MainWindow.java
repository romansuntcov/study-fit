    /**
     * Multiplication and addition
     * @param value1
     * @param value2
     * @param value3
     * @return value1 * value2 + value3
     */
    private String MulSum(String value1, String value2, String value3){
        if(value3 == null)
        {
            value3 = "0.0";
        }
        else if (value3.charAt(0) == '-'){
            if (value3.charAt(1) == '-')
            {      
            value3 = value3.substring(2);
            }
        }

        return Myformat.format(Double.parseDouble(value1) * Double.parseDouble(value2) + Double.parseDouble(value3));
    }
